/**
 * 
 */
public class TextureOptions : MonoBehaviour
{
    //********************************************************************//
    //***************************BEGIN VARIABLES**************************//
    //********************************************************************//
    //====================================================================//
    //                    PUBLIC VARIABLE DEFINITIONS                     //
    //====================================================================//
    public Dropdown textureDropdown;
    public Slider terrainTextureBrushRadius;
    public Slider terrainTextureBrushIntensity;
    //====================================================================//
    //                  END PUBLIC VARIABLE DEFINITIONS                   //
    //====================================================================//
    //====================================================================//
    //                    PRIVATE VARIABLE DEFINITIONS                    //
    //====================================================================//
    //====================================================================//
    //                  END PRIVATE VARIABLE DEFINITIONS                  //
    //====================================================================//
    //********************************************************************//
    //****************************END VARIABLES***************************//
    //********************************************************************//
    //********************************************************************//
    //****************************BEGIN METHODS***************************//
    //********************************************************************//
    //====================================================================//
    //                 MONOBEHAVIOR FUNCTION DEFINITIONS                  //
    //====================================================================//
    //====================================================================//
    //               END MONOBEHAVIOR FUNCTION DEFINITIONS                //
    //====================================================================//
    void Start()
    {
        // register callbacks to UI components
        terrainTextureBrushRadius.onValueChanged.AddListener(
            terrainTextureBrushRadiusCallback);

        terrainTextureBrushIntensity.onValueChanged.AddListener(
            terrainTextureBrushIntensityCallback);

        textureDropdown.onValueChanged.AddListener(textureDropdownCallback);
    }

    void OnEnable()
    {
        terrainTextureBrushRadiusCallback(terrainTextureBrushRadius.value);
        terrainTextureBrushIntensityCallback(terrainTextureBrushIntensity.value);
        textureDropdownCallback(textureDropdown.value);
        MessageDispatcher.SendMessageData("SET_STATE", "TerrainTexture");
    }

    void OnDisable()
    {

    }
    //====================================================================//
    //                     PUBLIC METHOD DEFINITIONS                      //
    //====================================================================//
    //====================================================================//
    //                   END PUBLIC METHOD DEFINITIONS                    //
    //====================================================================//
    //====================================================================//
    //                     PRIVATE METHOD DEFINITIONS                     //
    //====================================================================//
    //====================================================================//
    //                   END PRIVATE METHOD DEFINITIONS                   //
    //====================================================================//
    //********************************************************************//
    //*****************************END METHODS****************************//
    //********************************************************************//
    //********************************************************************//
    //******************************BEGIN ETC*****************************//
    //********************************************************************//
    //====================================================================//
    //                    HELPER FUNCTION DEFINITIONS                     //
    //====================================================================//

    void terrainTextureBrushRadiusCallback(float value)
    {
		MessageDispatcher.SendMessageData("SET_TERRAIN_BRUSH_RADIUS", value);
    }

    void terrainTextureBrushIntensityCallback(float value)
    {
		MessageDispatcher.SendMessageData("SET_TERRAIN_TEXTURE_STRENGTH", value);
    }

    void textureDropdownCallback(int value)
    {
		MessageDispatcher.SendMessageData("SET_TERRAIN_TEXTURE_TYPE", value);
    }
    //====================================================================//
    //                  END HELPER FUNCTION DEFINITIONS                   //
    //====================================================================//
    //********************************************************************//
    //*******************************END ETC******************************//
    //********************************************************************//

}
//----------------------------------------------------------------------------//
//                           END CLASS DEFINITIONS                            //
//----------------------------------------------------------------------------//
